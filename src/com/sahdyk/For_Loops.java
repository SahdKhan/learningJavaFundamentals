// #21
public class For_Loops {
    public static void main(String[] args){
        for (int i = 0; i < 5; i++)
        {
            System.out.println("Hello world! " + (i + 1));
        }
        /*
        BREAKDOWN
        i = THE COUNTER
        i < 5 = HOW MANY TIMES WE WANT THE LOOP (HOW MANY TIMES WE WANT THE CODE TO RUN)
        i++ = ADD ONE TO I EACH TIME CODE IS RAN TO EVENTUALLY STOP THE LOOP
         */
    }
}
